namespace System.Windows.Forms
{
    /// <devdoc>
    ///   Specifies the border style of a panel on the <see cref='System.Windows.Forms.StatusBar'/>.
    /// </devdoc>
    public enum StatusBarPanelBorderStyle
    {
        /// <devdoc>
        /// No border.
        /// </devdoc>
        None = 1,

        /// <devdoc>
        /// A raised border.
        /// </devdoc>
        Raised = 2,

        /// <devdoc>
        /// A sunken border.
        /// </devdoc>
        Sunken = 3,
    }
}
