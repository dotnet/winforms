namespace System.Windows.Forms
{
    /// <summary>
    ///  Specifies the border style of a panel on the <see cref='StatusBar'/>.
    /// </summary>
    public enum StatusBarPanelBorderStyle
    {
        /// <summary>
        ///  No border.
        /// </summary>
        None = 1,

        /// <summary>
        ///  A raised border.
        /// </summary>
        Raised = 2,

        /// <summary>
        ///  A sunken border.
        /// </summary>
        Sunken = 3,
    }
}
