namespace System.Windows.Forms
{
    /// <devdoc>
    /// Specifies the day of the week.
    /// </devdoc>
    public enum Day
    {
        /// <devdoc>
        /// The day Monday.
        /// </devdoc>
        Monday = 0,

        /// <devdoc>
        /// The day Tuesday.
        /// </devdoc>
        Tuesday = 1,

        /// <devdoc>
        /// The day Wednesday.
        /// </devdoc>
        Wednesday = 2,

        /// <devdoc>
        /// The day Thursday.
        /// </devdoc>
        Thursday = 3,

        /// <devdoc>
        /// The day Friday.
        /// </devdoc>
        Friday = 4,

        /// <devdoc>
        /// The day Saturday.
        /// </devdoc>
        Saturday  = 5,

        /// <devdoc>
        /// The day Sunday.
        /// </devdoc>
        Sunday = 6,

        /// <devdoc>
        /// A default day of the week specified by the application.
        /// </devdoc>
        Default = 7,
    }
}
