        /// <summary>
        ///  When DropDown is disposed; nullify the dropDown.
        /// </summary>
        private void OnDropDownDisposed(object sender, EventArgs e)
        {
            if (MenuItem != null)
            {
                if (MenuItem.DropDown != null)
                {
                    MenuItem.DropDown.Disposed -= new EventHandler(OnDropDownDisposed);
